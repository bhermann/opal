package tactest;

/**
 * Class with simple methods containing switch statements.
 * 
 * @author Roberts Kolosovs
 *
 */
public class SwitchStatements {

	int shortSwitch(int a) {
		switch (a) {
		case 1:
			return 1;
		case 2:
			return 2;
		case 3:
			return 3;
		default:
			return 0;
		}
	}

	int longSwitch(int a) {
		switch (a) {
		case 1:
			return 1;
		case 2:
			return 2;
		case 3:
			return 3;
		case 4:
			return 4;
		case 5:
			return 5;
		case 6:
			return 6;
		case 7:
			return 7;
		case 8:
			return 8;
		case 9:
			return 9;
		case 10:
			return 10;
		case 11:
			return 11;
		default:
			return 0;
		}
	}

}
